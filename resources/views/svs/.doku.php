In diesem Ordner liegen alle PHP Seiten, die angezeigt werden, wenn ein Benutzer eine Website aufruft.

Unter svsNew/views/layouts findet man die Datei app.blade.php

Dies ist ein Layout, der für jede Seite benutzt wird, um den Header mit den Buttons für die Verwaltung, Dashboard und Home anzuzigen und die login und register optionen.
